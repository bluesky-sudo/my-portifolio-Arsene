// Dark Mode Functionality
function initDarkMode() {
    const darkModeToggle = document.getElementById('darkModeToggle');
    const body = document.body;
    
    // Check for saved theme preference or default to light mode
    const currentTheme = localStorage.getItem('theme') || 'light';
    body.setAttribute('data-theme', currentTheme);
    
    // Update toggle button state
    updateDarkModeToggle(currentTheme === 'dark');
    
    // Toggle dark mode
    darkModeToggle.addEventListener('click', () => {
        const currentTheme = body.getAttribute('data-theme');
        const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
        
        body.setAttribute('data-theme', newTheme);
        localStorage.setItem('theme', newTheme);
        updateDarkModeToggle(newTheme === 'dark');
        
        // Add smooth transition
        body.style.transition = 'background-color 0.3s ease, color 0.3s ease';
        setTimeout(() => {
            body.style.transition = '';
        }, 300);
    });
}

function updateDarkModeToggle(isDark) {
    const darkModeToggle = document.getElementById('darkModeToggle');
    const icon = darkModeToggle.querySelector('i');
    
    if (isDark) {
        darkModeToggle.classList.add('active');
        icon.className = 'fas fa-sun';
        darkModeToggle.setAttribute('aria-label', 'Switch to light mode');
    } else {
        darkModeToggle.classList.remove('active');
        icon.className = 'fas fa-moon';
        darkModeToggle.setAttribute('aria-label', 'Switch to dark mode');
    }
}

// Falling Stars Animation
function initFallingStars() {
    const fallingStarsContainer = document.getElementById('fallingStars');
    
    function createStar() {
        const star = document.createElement('div');
        star.className = 'falling-star';
        
        // Random position and timing
        const startX = Math.random() * window.innerWidth;
        const delay = Math.random() * 3;
        const duration = 2 + Math.random() * 2;
        
        star.style.left = startX + 'px';
        star.style.animationDelay = delay + 's';
        star.style.animationDuration = duration + 's';
        
        fallingStarsContainer.appendChild(star);
        
        // Remove star after animation
        setTimeout(() => {
            if (star.parentNode) {
                star.remove();
            }
        }, (delay + duration) * 1000);
    }
    
    // Create stars periodically
    setInterval(createStar, 300);
    
    // Create initial stars
    for (let i = 0; i < 10; i++) {
        setTimeout(createStar, i * 200);
    }
}

// Space Effects
function initSpaceEffects() {
    // Add parallax effect to sections
    window.addEventListener('scroll', () => {
        const scrolled = window.pageYOffset;
        const parallaxElements = document.querySelectorAll('.hero, .about, .skills, .projects, .experience, .contact');
        
        parallaxElements.forEach((element, index) => {
            const rate = scrolled * (0.1 + index * 0.02);
            element.style.transform = `translateY(${rate}px)`;
        });
    });
    
    // Add floating animation to cards
    const cards = document.querySelectorAll('.skill-category, .project-card, .timeline-content');
    cards.forEach((card, index) => {
        card.style.animationDelay = `${index * 0.1}s`;
        card.classList.add('float-animation');
    });
}

